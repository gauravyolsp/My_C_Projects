int main(void)
{
	char szStr[20];

	printf("\n\nEnter The String:\t");
	gets(szStr);

	MyStrlwr(szStr);

	printf("\nString In Lower Case Is:\t");
	puts(szStr);

	return 0;
}

char *MyStrlwr(char *szStr)
{
	int iCounter;
	
	for(iCounter = 0; szStr[iCounter] != '\0'; iCounter++)
	{
		if(szStr[iCounter] >= 65 && szStr[iCounter] <= 90)
			szStr[iCounter] = szStr[iCounter] + 32;
	}

	return szStr;
}
