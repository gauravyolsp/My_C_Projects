char *MyStrcpy(char *, const char *);

////////////////////////////////////////////////////////////////////////////////////
//	FUNCTION DEFINATION
////////////////////////////////////////////////////////////////////////////////////

int main(void)
{
	char szSrc[20];
	char szDest[20];

	printf("\n\nEnter Source String:\t");
	gets(szSrc);

	printf("\n\nSource String Is:\t");
	puts(szSrc);

	MyStrcpy(szDest, szSrc);

	printf("\n\nDestination String Is:\t");
	puts(szDest);

	return 0;
}

char *MyStrcpy(char *pDest, const char *pSrc)
{
	int iCounter;
	
	for(iCounter = 0; pSrc[iCounter] != '\0'; iCounter++) 
		pDest[iCounter] = pSrc[iCounter];

	pDest[iCounter] = '\0';

	return pDest;
}
