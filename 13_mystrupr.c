int main(void)
{
	char szStr[20];

	printf("\n\nEnter The String:\t");
	gets(szStr);

	MyStrupr(szStr);

	printf("\nString in upper case is: %s\n", szStr);

	return 0;
}

char *MyStrupr(char *szStr)
{
	int iCounter;
	
	for(iCounter = 0; szStr[iCounter] != '\0'; iCounter++)
	{
		if(szStr[iCounter] >= 97 && szStr[iCounter] <= 122)
			szStr[iCounter] = szStr[iCounter] - 32;
	}

	return szStr;
}
